/**
 * @name add
 *
 * @function
 *
 * @description
 * Add two numbers and return the sum
 * @param {number} a
 * @param {number} b
 *
 * @returns {number}
 *
 * @example
 * add(1,3)
 * // => 4
 *
 */

const add = (a, b) => {
  return a + b
}

/**
 * @name subtract
 *
 * @function
 *
 * @description
 * Subtract two numbers and return the difference
 * @param {number} a
 * @param {number} b
 *
 * @returns {number}
 *
 * @example
 * subtract(4,2)
 * // => 2
 *
 */

const subtract = (a, b) => {
  return a - b
}

export {
  add,
  subtract,
}